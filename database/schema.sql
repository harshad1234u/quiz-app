-- ============================================
-- AI Quiz Application – PostgreSQL Schema (Supabase)
-- ============================================
-- Run this ONCE in the Supabase SQL Editor.
-- Safe to re-run (uses IF NOT EXISTS / IF EXISTS).

-- ------------------------------------------
-- 1. Users
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS users (
    user_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name          TEXT          NOT NULL,
    email         TEXT          NOT NULL UNIQUE,
    password      TEXT          DEFAULT NULL,   -- NULL when signed-in via Google
    google_id     TEXT          DEFAULT NULL UNIQUE,
    role          TEXT          NOT NULL DEFAULT 'user' CHECK (role IN ('admin', 'user')),
    avatar_url    TEXT          DEFAULT NULL,
    selected_topics JSONB      DEFAULT NULL,    -- e.g. ["Cybersecurity","AI & Machine Learning"]
    created_at    TIMESTAMPTZ   DEFAULT now(),
    updated_at    TIMESTAMPTZ   DEFAULT now()
);

-- If users table already exists but is missing selected_topics, add it:
ALTER TABLE users ADD COLUMN IF NOT EXISTS selected_topics JSONB DEFAULT NULL;

-- Auto-update updated_at on row change
CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = now();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trg_users_updated_at ON users;
CREATE TRIGGER trg_users_updated_at
    BEFORE UPDATE ON users
    FOR EACH ROW EXECUTE FUNCTION update_updated_at();

-- ------------------------------------------
-- 2. User Interests (kept for backward compat)
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS user_interests (
    interest_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id       BIGINT        NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    interest_name TEXT          NOT NULL,
    UNIQUE (user_id, interest_name)
);

-- ------------------------------------------
-- 3. Categories
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS categories (
    category_id   BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_name TEXT          NOT NULL UNIQUE,
    description   TEXT          DEFAULT NULL,
    icon          TEXT          DEFAULT '📚',
    created_at    TIMESTAMPTZ   DEFAULT now()
);

-- ------------------------------------------
-- 4. Questions
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS questions (
    question_id    BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    category_id    BIGINT        NOT NULL REFERENCES categories(category_id) ON DELETE CASCADE,
    question_text  TEXT          NOT NULL,
    options        JSONB         NOT NULL DEFAULT '[]',
    correct_answer TEXT          NOT NULL,
    explanation    TEXT          DEFAULT NULL,
    difficulty     TEXT          NOT NULL DEFAULT 'Medium' CHECK (difficulty IN ('Easy', 'Medium', 'Hard')),
    created_at     TIMESTAMPTZ   DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_questions_cat_diff ON questions (category_id, difficulty);

-- ------------------------------------------
-- 5. Quiz Sessions (one row per attempt)
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS quiz_sessions (
    session_id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id          BIGINT        NOT NULL REFERENCES users(user_id) ON DELETE CASCADE,
    category_id      BIGINT        NOT NULL REFERENCES categories(category_id) ON DELETE CASCADE,
    difficulty       TEXT          NOT NULL DEFAULT 'Medium' CHECK (difficulty IN ('Easy', 'Medium', 'Hard')),
    score            INT           NOT NULL DEFAULT 0,
    total_questions  INT           NOT NULL DEFAULT 0,
    time_taken       INT           DEFAULT NULL,   -- seconds
    date_taken       TIMESTAMPTZ   DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_sessions_user    ON quiz_sessions (user_id);
CREATE INDEX IF NOT EXISTS idx_sessions_leaderboard ON quiz_sessions (user_id, score DESC);

-- ------------------------------------------
-- 6. Quiz Answers (one row per question)
-- ------------------------------------------
CREATE TABLE IF NOT EXISTS quiz_answers (
    answer_id        BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    session_id       BIGINT        NOT NULL REFERENCES quiz_sessions(session_id) ON DELETE CASCADE,
    question_id      BIGINT        NOT NULL REFERENCES questions(question_id) ON DELETE CASCADE,
    selected_answer  TEXT          DEFAULT NULL,
    is_correct       BOOLEAN       NOT NULL DEFAULT FALSE
);

-- ------------------------------------------
-- RLS: Enable + open policies (app-managed auth via anon key)
